function getChatLocale() {
  const source = window.PURETALENT_CHATBOT;
  return source?.locales?.[currentLang] || source?.locales?.en || {};
}

function normalizeText(value) {
  return String(value || "")
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[؟?.,،؛:!()"'`]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function scoreKnowledgeEntry(entry, question) {
  const normalizedQuestion = normalizeText(question);
  const tokens = normalizedQuestion.split(" ").filter((token) => token.length > 1);
  let score = 0;

  [...(entry.questions || []), ...(entry.keywords || [])].forEach((phrase) => {
    const normalizedPhrase = normalizeText(phrase);
    if (!normalizedPhrase) return;
    if (normalizedQuestion.includes(normalizedPhrase)) score += normalizedPhrase.length > 8 ? 7 : 4;
    normalizedPhrase.split(" ").forEach((word) => {
      if (word.length > 1 && tokens.includes(word)) score += 2;
    });
  });

  return score;
}

function findChatAnswer(question) {
  const source = window.PURETALENT_CHATBOT;
  if (!source?.knowledge?.length) return null;

  const ranked = source.knowledge
    .map((entry) => ({ entry, score: scoreKnowledgeEntry(entry, question) }))
    .sort((a, b) => b.score - a.score);

  return ranked[0]?.score > 0 ? ranked[0].entry : null;
}

function addChatMessage(type, content, action) {
  const messages = document.getElementById("chatbotMessages");
  if (!messages) return null;

  const bubble = document.createElement("div");
  bubble.className = `chatbot-message ${type}`;
  bubble.textContent = content;

  if (action) {
    const locale = getChatLocale();
    const link = document.createElement("a");
    link.className = "chatbot-action";
    link.href = action;
    link.textContent = locale.actionText || "Open section";
    link.addEventListener("click", () => toggleChat(false));
    bubble.appendChild(link);
  }

  messages.appendChild(bubble);
  messages.scrollTop = messages.scrollHeight;
  return bubble;
}

function answerChatQuestion(question) {
  const source = window.PURETALENT_CHATBOT;
  const locale = getChatLocale();
  const typing = addChatMessage("bot typing", locale.typing || "Searching...");

  window.setTimeout(() => {
    typing?.remove();
    const match = findChatAnswer(question);
    const answer = match?.answers?.[currentLang] || match?.answers?.en || locale.fallback || "I do not have an answer yet.";
    addChatMessage("bot", answer, match?.action);
  }, 420);
}

function renderChatSuggestions() {
  const source = window.PURETALENT_CHATBOT;
  const container = document.getElementById("chatbotSuggestions");
  if (!container) return;

  const questions = source?.quickQuestions?.[currentLang] || source?.quickQuestions?.en || [];
  container.innerHTML = "";
  questions.forEach((question) => {
    const button = document.createElement("button");
    button.type = "button";
    button.textContent = question;
    button.addEventListener("click", () => {
      addChatMessage("user", question);
      answerChatQuestion(question);
    });
    container.appendChild(button);
  });
}

function applyChatLanguage() {
  const locale = getChatLocale();
  document.querySelectorAll("[data-chat]").forEach((node) => {
    const value = locale[node.dataset.chat];
    if (typeof value === "string") node.textContent = value;
  });

  document.querySelectorAll("[data-chat-placeholder]").forEach((node) => {
    const value = locale[node.dataset.chatPlaceholder];
    if (typeof value === "string") node.setAttribute("placeholder", value);
  });

  renderChatSuggestions();

  const messages = document.getElementById("chatbotMessages");
  if (messages && !messages.children.length) {
    addChatMessage("bot", locale.welcome || "Hello. How can I help?");
  }
}

function toggleChat(forceOpen) {
  const panel = document.getElementById("chatbotPanel");
  const launcher = document.getElementById("chatbotLauncher");
  if (!panel || !launcher) return;

  const open = typeof forceOpen === "boolean" ? forceOpen : !panel.classList.contains("open");
  panel.classList.toggle("open", open);
  panel.setAttribute("aria-hidden", String(!open));
  launcher.setAttribute("aria-expanded", String(open));
  chatState.opened = open;

  if (open) {
    window.setTimeout(() => document.getElementById("chatbotInput")?.focus(), 180);
  }
}

function setupChatbot() {
  if (chatState.initialized) return;
  chatState.initialized = true;

  const launcher = document.getElementById("chatbotLauncher");
  const close = document.getElementById("chatbotClose");
  const form = document.getElementById("chatbotForm");
  const input = document.getElementById("chatbotInput");

  launcher?.addEventListener("click", () => toggleChat());
  close?.addEventListener("click", () => toggleChat(false));
  form?.addEventListener("submit", (event) => {
    event.preventDefault();
    const question = input.value.trim();
    if (!question) return;
    input.value = "";
    addChatMessage("user", question);
    answerChatQuestion(question);
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") toggleChat(false);
  });
}

